package tp2.ejercicio1;

public class DobleEntero {

    private int sum;
    private int dif;

    public DobleEntero(int sum, int dif) {
        this.sum = sum;
        this.dif = dif;
    }

    public int getSum()
    {
        return sum;
    }

    public int getDif()
    {
        return dif;
    }
    
}
